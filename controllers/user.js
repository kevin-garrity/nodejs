/**
 * POST /signup
 * Create a new local account.
 */
exports.postSignup = function(req, res, next) {
  req.assert('email', 'Email is not valid').isEmail();
  req.assert('password', 'Password must be at least 4 characters long').len(4);
  // req.assert('confirmPassword', 'Passwords do not match').equals(req.body.password);
  
  var errors = req.validationErrors();

  if (errors) {
    req.flash('errors', errors);
    return res.redirect('/signup');
  }

  var price;
  if(req.body.price){
    price = parseFloat(req.body.price.replace(/[^0-9.]/g,'')).toFixed(2).replace(".","");
  } else {
    price = 0;
  }

  var user = new User({
    email: req.body.email,
    password: req.body.password,
    role: req.body.role,
    customid: parseInt(req.body.email, 36) + Date.now() //Random unique ID
  });

  User.findOne({ email: req.body.email }, function(err, existingUser) {
    if (existingUser) {
      req.flash('errors', { msg: 'Account with that email address already exists.' });
      return res.redirect('/signup');
    }
    user.save(function(err) {
      if (err) return next(err);
      // sendEmail(user.email, user.profile.name, 'Hey ' + user.profile.name + '\n\nThanks for signing up! We\'re still field testing our system, so please let us know if you have any questions or concerns.\n\nCheers,\nNaeem, Cofounder', 'Congratulations on signing up!');
      req.logIn(user, function(err) {
        if (err) return next(err);
        if(user.role == 'Player')
          res.redirect('/');
        else
          res.redirect('/account');
      });
    });
  });
};

